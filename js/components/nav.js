    /* ── BUILD ────────────────────────────────────────────────── */
    function buildNav() {
        const nav = document.getElementById('sidebar-nav');
        if (!nav) return;
        const isEmployee = window.GMRole === 'employee';
        let html = '';
        NAV_ITEMS.forEach(item => {
            // hide admin-only items from employees
            if (item.adminOnly && isEmployee) return;
            if (item.section) {
                html += `<div class="sidebar__section-label">${item.section}</div>`;
            } else {
                const icon = ICONS[item.hash] || ICONS.dashboard;
                html += `<a class="sidebar__link" href="#${item.hash}" data-hash="${item.hash}" role="button">
          <span class="nav-icon">${icon}</span>
          <span class="nav-label">${item.label}</span>
        </a>`;
            }
        });
        nav.innerHTML = html;
    }

    /* ── ACTIVE STATE ────────────────────────────────────────── */
    function highlightNav(hash) {
        document.querySelectorAll('.sidebar__link').forEach(a => {
            const h = a.getAttribute('data-hash') || '';
            a.classList.toggle('active', h === hash);
        });
    }

    buildNav();
    window.GMNav = { highlightNav };
